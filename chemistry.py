def getMass(ZA):
    Z = ZA // 1000 
    A = ZA % 1000
    name = getElement(Z)
    return float(masses[ str(Z) + '-' + name + '-' + str(A)]) * 939565421 # в эВ


def getMassExited(ZA, MT):
    Z = ZA // 1000 
    A = ZA % 1000
    name = getElement(Z)
    return float(masses[ str(Z) + '-' + name + '-' + str(A) + '*' + str(MT-50)]) * 939565421 # в эВ


def getZ(ele):
    return int(elements[ele.capitalize()])


def getElement(z):
    for ele in elements:
        if elements[ele] == z:
            return ele
    return "None"

